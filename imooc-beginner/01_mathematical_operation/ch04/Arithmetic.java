public class Arithmetic{ 
    public static void main(String[] args){ 
        System.out.println("This is arithmetic program.");

        int x_div = 6;
        int y_div = 4;
        System.out.println(x_div/y_div);

        int x_module = 7;
        int y_module = 3;
        System.out.println(x_module/y_module);
        System.out.println(x_module%y_module);
    }
}
